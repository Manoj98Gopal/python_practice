print("---------------------Arithmetic operator------------------------")

a = 10
b = 23 

add = a + b

sub = a - b

mul = a * b

div = a / b

mod = a % b

sq = a ** b

print(add)
print(sub)
print(mul)
print(div)
print(mod)
print(sq)


